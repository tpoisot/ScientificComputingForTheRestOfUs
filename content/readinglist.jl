# This material is a lot more useful when paired with additional readings. What
# follows is an uncurated list of things that we consulted when writing and
# revising it, and are both accessible and illuminating. The point is not to
# read all of them in one go. The point is not to provide a complete reference
# bibliography either. In a classroom context, a lot of these articles would be
# on the syllabus. In a self-learning environment, it's useful to know where to
# look for things to read, and we encourage you to have a look at a few of these
# entries.

# One interesting essay to get you motivated to read more about computational
# approaches is "All biology is computational biology":

# !!!REF Markowetz2017All

# The first step is often to start with "good enough" practices:

# !!!REF Wilson2017Good

# If you want a more in-depth treatment of best practices, there is a must-read:

# !!!REF Wilson2014Best

# But when facing a lot of possible action items, knowing what is barely
# sufficient is immensely helpful:

# !!!REF Lee2021Barely

# These practices are important, because clean code facilitates communication
# among researchers:

# !!!REF Filazzola2022Call

# If you want to *release* some code, there are articles about the type of
# additional checks to perform:

# !!!REF Poisot2015Best

# If you question the importance of releasing your code, this article should
# definitely convince you:

# !!!REF Mislan2016Elevating

# There is also a vast amount of "ten simple rules" papers about various sides
# of computational science.

# Not sure how to make your research more reproductible?

# !!!REF Sandve2013Ten

# Want to take explore different ways to store your digital data for analysis
# and archival?

# !!!REF Hart2016Ten

# Interesting in feeding your data to make them grow?

# !!!REF Goodman2014Ten

# Ready to move into fully open software development?

# !!!REF Prlic2012Ten

# Want to make your software more robust?

# !!!REF Taschuk2017Ten

# More usable, maybe?

# !!!REF List2017Ten

# How about making your computational research more effective?

# !!!REF Osborne2014Ten

# Some of these papers also offer good baselines about your expectation when
# learning programming from a non-CS background:

# !!!REF Carey2018Ten

# Learning on your own? There are guidelines for this!

# !!!REF Lawlor2022Ten

# Looking to assemble a group of friends to build a community? You guessed it, guidelines!

# !!!REF Stevens2018Building

# More into books? ["The pragmatic programmer"][pragm] is a masterpiece. I have
# also heard great things about ["Clean code"][cleanc]. The online book ["How to
# think like a computer scientist"][httlacs] is based on Julia, and very
# thorough. Finally, ["Hands-on design patterns and best practices with
# Julia"][handson] is a wonderfully accessible book that will make you a better
# programmer, even if *Julia* is not your main language.

